#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	i;

	if (nb < 10)
	{
		i = (nb % 10) + '0';
		write(1, &i, 1);
	}
	else
	{
		i = nb / 10;
		ft_putnbr(i);
		i = (nb % 10) + '0';
		write(1, &i, 1);
	}
}
