char	*ft_strcat(char *dest, char *src)
{
	int	d;
	int	i;

	i = 0;
	d = 0;
	while (*(dest + d))
	{
		d++;
	}
	while (*(src + i))
	{
		*(dest + d) = *(src + i);
		i++;
		d++;
	}
	*(dest + d) = '\0';
	return (dest);
}
