void	ft_rev_int_tab(int *tab, int size)
{
	int	l;
	int	h;

	l = 0;
	while (l < (size / 2))
	{
		h = tab[l];
		tab[l] = tab[size - 1 - l];
		tab[size - 1 - l] = h;
		l++;
	}
}
